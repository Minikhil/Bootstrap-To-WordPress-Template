            <h1>University Policy for Waiving Medical Insurance</h1>
 			The University requires all students taking 12 credits undergraduate or 9 hours graduate study to have adequate health insurance coverage. If you have private coverage through your parents, spouse or employer, you may qualify for a waiver of the University's plan requirement. <br /><br />
             
            <strong>To Qualify for this Waiver</strong>: Your private insurance must be in effect on or before September 1, 2010 and cover:
			<ul>
				<li>Doctor and Specialist office visits in the Western New York Buffalo Area</li>
				<li>X-ray and lab coverage in the Western New York Buffalo area</li>
				<li>Mental health outpatient/inpatient coverage in the Western New York Buffalo area</li>
				<li>Prescription coverage</li>
			</ul>
            
            <h2>How to Waive</h2>
            Domestic students must perform the waiver online <a href="domestic_waiver.php">here</a>.<br />
            International exchange students must send a paper waiver form which can be found <a href="international_waiver.php">here</a> along with a copy of their private insurance card to the Student Medical Insurance Office.
            
            <h2>Things to Consider Before Waiving</h2>
			<ul>
				<li>Will your plan cover you for a full academic year and summer?</li>
				<li>Will your plan cover both at home and away at school?</li>
				<li>Will your plan end when you turn 19, 21, 23?</li>
				<li>If so, will you be paying high COBRA premiums to continue coverage?</li>
				<li>Will your plan cover you if you registered as a part-time student for a semester?</li>
				<li>Are you covered for medical care while traveling?</li>
				<li>Do you have a high deductible to meet or many copays?</li>
				<li>Are you covered for illness or accidents outside your plan area such as:</li>
					<ul>
						<li>Strep throat?</li>
						<li>Sprained Ankle?</li>
						<li>Upper respiratory infection?</li>
						<li>Urinary tract infection?</li>
						<li>Specialist visits?</li>
						<li>Eye infection or injury?</li>
						<li>Chiropractic care?</li>
						<li>Laboratory tests?</li>
						<li>X-rays?</li>
						<li>Ambulance services?</li>
					</ul>
				<li>Are you required to get a referral/authorization from your doctor at home in order for services at school to be paid by your insurance?</li>
				<li>Will your plan cover prescriptions at school?</li>
				<li>Are there doctors in the Buffalo area you can use with your plan?</li>
				<li>Are you covered for mental/behavioral health benefits at school?</li>
